class MLP1D(nn.Module):
    def __init__(self, dim, dim_factor, time_emb_dim, num_blocks=1, hidden_dim = None):
        super().__init__()
        self.hidden_dim = dim * dim_factor if hidden_dim is None else hidden_dim
        self.channels = 1
        self.self_condition = None
        sinu_pos_emb = SinusoidalPosEmb(time_emb_dim)

        self.time_mlp = nn.Sequential(
            sinu_pos_emb,
            nn.Linear(time_emb_dim, self.hidden_dim),
            nn.GELU(),
            nn.Linear(self.hidden_dim, self.hidden_dim),
            nn.SiLU(),
            nn.Linear(self.hidden_dim, self.hidden_dim * 2)
        )

        # Two linear layers form an inverse bottleneck block
        self.fc1 = nn.Linear(dim, self.hidden_dim)
        self.fc2 = nn.Linear(self.hidden_dim, self.hidden_dim)
        self.fc3 = nn.Linear(self.hidden_dim, dim)
        self.act1 = nn.GELU()
        self.act2 = nn.SiLU()

        self.norm1 = nn.LayerNorm(self.hidden_dim)
        self.norm2 = nn.LayerNorm(self.hidden_dim)

