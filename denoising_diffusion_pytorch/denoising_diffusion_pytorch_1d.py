def cal_exact_score(data, alpha, x):
    # print("data shape", data.shape)
    # print("x shape", x.shape)
    # print("alpha shape", alpha.shape)
    # import pdb; pdb.set_trace()
    _data = normalize_to_neg_one_to_one(data)
    # Ensure broadcasting works correctly across batch and feature dimensions
    diff = _data * torch.sqrt(alpha) - x
    d2 = torch.sum(diff**2, axis=-1) / (2*(1-alpha.reshape(-1, 1)))

    # import pdb; pdb.set_trace()
    # Normalizing within each batch
    mind2, _ = torch.min(d2, dim=1, keepdim=True)
    d2 -= mind2
    kernel = torch.exp(-d2)
    kernel /= torch.sum(kernel, dim=1, keepdim=True)

    # Weighted sum for each batch
    weight_x0 = kernel @ _data
    weight_x0 = weight_x0.unsqueeze(1)

    # Compute score for each batch, ensuring output shape is (B, 1, D)
    score = x / torch.sqrt(1-alpha) - torch.sqrt(alpha / (1-alpha)) * weight_x0
    # print(score.shape)
    return score


# trainer class
class Trainer1D(object):
    def __init__(
        self,
        diffusion_model: GaussianDiffusion1D,
        dataset: Dataset,
        *,
        train_batch_size = 16,
        gradient_accumulate_every = 1,
        train_lr = 1e-4,
        train_num_steps = 100000,
        ema_update_every = 10,
        ema_decay = 0.995,
        adam_betas = (0.9, 0.99),
        save_and_sample_every = 1000,
        num_samples = 25,
        results_folder = './results',
        amp = False,
        mixed_precision_type = 'fp16',
        split_batches = True,
        max_grad_norm = 1.,
        see_every = 1000,
        save_every = 5000,
        # analytic_tgt = False,
        # data_tensor = None
    ):
        super().__init__()

        # accelerator

        self.accelerator = Accelerator(
            split_batches = split_batches,
            mixed_precision = mixed_precision_type if amp else 'no'
        )

        # model
        self.model = diffusion_model
        self.channels = diffusion_model.channels

        # sampling and training hyperparameters

        assert has_int_squareroot(num_samples), 'number of samples must have an integer square root'
        self.num_samples = num_samples
        self.save_and_sample_every = save_and_sample_every
        self.save_every = save_every
        self.see_every = see_every

        self.batch_size = train_batch_size
        self.gradient_accumulate_every = gradient_accumulate_every
        self.max_grad_norm = max_grad_norm

        self.train_num_steps = train_num_steps

        # dataset and dataloader
        self.ds = dataset
        sampler = RandomSampler(self.ds, replacement=True, num_samples=train_num_steps * train_batch_size)
        # dl = DataLoader(self.ds, batch_size=train_batch_size, sampler=sampler, pin_memory=True, num_workers=cpu_count())
        dl = DataLoader(self.ds, batch_size=train_batch_size, sampler=sampler, pin_memory=True, num_workers=1)
        dl = self.accelerator.prepare(dl)
        self.dl = cycle(dl)

        # optimizer

        self.opt = Adam(diffusion_model.parameters(), lr = train_lr, betas = adam_betas)

        # for logging results in a folder periodically

        if self.accelerator.is_main_process:
            self.ema = EMA(diffusion_model, beta = ema_decay, update_every = ema_update_every)
            self.ema.to(self.device)

        self.results_folder = Path(results_folder)
        self.results_folder.mkdir(exist_ok = True)

        # step counter state

        self.step = 0

        # prepare model, dataloader, optimizer with accelerator

        self.model, self.opt = self.accelerator.prepare(self.model, self.opt)


    @property
    def device(self):
        return self.accelerator.device

    def save(self, milestone):
        if not self.accelerator.is_local_main_process:
            return

        data = {
            'step': self.step,
            'model': self.accelerator.get_state_dict(self.model),
            'opt': self.opt.state_dict(),
            'ema': self.ema.state_dict(),
            'scaler': self.accelerator.scaler.state_dict() if exists(self.accelerator.scaler) else None,
            'version': __version__
        }

        torch.save(data, str(self.results_folder / f'model-{milestone}.pt'))

    def load(self, milestone):
        accelerator = self.accelerator
        device = accelerator.device

        data = torch.load(str(self.results_folder / f'model-{milestone}.pt'), map_location=device)

        model = self.accelerator.unwrap_model(self.model)
        model.load_state_dict(data['model'])

        self.step = data['step']
        self.opt.load_state_dict(data['opt'])
        if self.accelerator.is_main_process:
            self.ema.load_state_dict(data["ema"])

        if 'version' in data:
            print(f"loading from version {data['version']}")

        if exists(self.accelerator.scaler) and exists(data['scaler']):
            self.accelerator.scaler.load_state_dict(data['scaler'])

    def train(self):
        accelerator = self.accelerator
        device = accelerator.device

        with tqdm(initial = self.step, total = self.train_num_steps, disable = not accelerator.is_main_process) as pbar:

            while self.step < self.train_num_steps:

                total_loss = 0.

                for _ in range(self.gradient_accumulate_every):
                    data = next(self.dl).to(device)

                    with self.accelerator.autocast():
                        # print(data.shape)
                        # import pdb; pdb.set_trace()
                        loss = self.model(data)
                        loss = loss / self.gradient_accumulate_every
                        total_loss += loss.item()

                    self.accelerator.backward(loss)

                pbar.set_description(f'loss: {total_loss:.4f}')

                accelerator.wait_for_everyone()
                accelerator.clip_grad_norm_(self.model.parameters(), self.max_grad_norm)

                self.opt.step()
                self.opt.zero_grad()

                accelerator.wait_for_everyone()

                self.step += 1
                if accelerator.is_main_process:
                    self.ema.update()
                    milestone = self.step // self.see_every
                    if self.step != 0 and self.step % self.see_every == 0:
                        self.ema.ema_model.eval()

                        with torch.no_grad():
                            batches = num_to_groups(self.num_samples, self.batch_size)
                            all_samples_list = list(map(lambda n: self.ema.ema_model.sample(batch_size=n), batches))

                        all_samples = torch.cat(all_samples_list, dim = 0)
                        print(f"vis shape {all_samples.shape}")
                        # torch.save(all_samples, str(self.results_folder / f'sample-{milestone}.png'))
                        b = all_samples.shape[0]
                        grid_size = int(math.sqrt(b))

                        # Create a figure with subplots
                        plt.figure(figsize=(10, 10))
                        for i in range(b):
                            plt.subplot(grid_size, grid_size, i + 1)
                            plt.plot(all_samples[i, 0, :].cpu().numpy())
                            plt.tight_layout()
                        plt.savefig(str(self.results_folder / f'sample-{milestone}.png'))
                        plt.close()

                        # repeated sample 128 samples, and calculate the statistics
                        with torch.no_grad():
                            batches = num_to_groups(256, 64)
                            all_samples_list = list(map(lambda n: self.ema.ema_model.sample(batch_size=n), batches))
                        all_samples = torch.cat(all_samples_list, dim=0)
                        print(all_samples.shape)
                        tot_stat, avg_margin = statistic_function(all_samples)
                        tot_stat = tot_stat.float() / 256
                        # Save to results folder/log.txt file
                        stat_list = tot_stat.cpu().tolist()
                        stat_list.extend([0 for _ in range(all_samples.shape[2] - len(stat_list))])
                        with open(str(self.results_folder / 'log.txt'), 'a') as f:
                            f.write(f'{self.step} {avg_margin.item():03f} \n {stat_list} \n')

                    if self.step != 0 and self.step % self.save_every == 0:
                        self.save(milestone)

                pbar.update(1)

        accelerator.print('training complete')

